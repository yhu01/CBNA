def get_resume_file(output_dir):
    filelist = glob.glob(os.path.join(output_dir, '*.pth'))
    if len(filelist) == 0:
        return None

    filelist =  [ x  for x in filelist if os.path.basename(x) != 'best.pth' ]
    epochs = np.array([int(os.path.splitext(os.path.basename(x))[0]) for x in filelist])
    max_epoch = np.max(epochs)
    resume_file = os.path.join(output_dir, '{:d}.pth'.format(max_epoch))
    return resume_file


def get_best_file(checkpoint_dir):    
    best_file = os.path.join(checkpoint_dir, 'best.pth')
    if os.path.isfile(best_file):
        return best_file
    else:
        return get_resume_file(checkpoint_dir)
    
    
